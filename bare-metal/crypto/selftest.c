#include "sha256.h"
#include "chacha20poly1305.h"
#include "x25519.h"
#include "../lib/printf.h"
#include <string.h>

/* Boot-time crypto self-tests against published vectors (FIPS 180-4,
 * RFC 4231, RFC 8439, RFC 7748). If any FAIL, the TLS handshake built on
 * top cannot possibly work — better to know here. */

static int eq(const uint8_t *a, const uint8_t *b, int n) {
    for (int i = 0; i < n; i++) if (a[i] != b[i]) return 0;
    return 1;
}

int crypto_selftest(void) {
    int ok = 1;

    /* SHA-256("abc") */
    {
        uint8_t h[32];
        static const uint8_t want[32] = {
            0xba,0x78,0x16,0xbf,0x8f,0x01,0xcf,0xea,0x41,0x41,0x40,0xde,0x5d,0xae,0x22,0x23,
            0xb0,0x03,0x61,0xa3,0x96,0x17,0x7a,0x9c,0xb4,0x10,0xff,0x61,0xf2,0x00,0x15,0xad};
        sha256("abc", 3, h);
        int p = eq(h, want, 32); ok &= p;
        printf("[crypto] sha256   %s\n", p ? "ok" : "FAIL");
    }

    /* HMAC-SHA256 (RFC 4231 case 1) */
    {
        uint8_t key[20], out[32];
        memset(key, 0x0b, 20);
        static const uint8_t want[32] = {
            0xb0,0x34,0x4c,0x61,0xd8,0xdb,0x38,0x53,0x5c,0xa8,0xaf,0xce,0xaf,0x0b,0xf1,0x2b,
            0x88,0x1d,0xc2,0x00,0xc9,0x83,0x3d,0xa7,0x26,0xe9,0x37,0x6c,0x2e,0x32,0xcf,0xf7};
        hmac_sha256(key, 20, (const uint8_t *)"Hi There", 8, out);
        int p = eq(out, want, 32); ok &= p;
        printf("[crypto] hmac     %s\n", p ? "ok" : "FAIL");
    }

    /* ChaCha20-Poly1305 AEAD (RFC 8439 section 2.8.2) */
    {
        uint8_t key[32], nonce[12] = {0x07,0,0,0,0x40,0x41,0x42,0x43,0x44,0x45,0x46,0x47};
        uint8_t aad[12] = {0x50,0x51,0x52,0x53,0xc0,0xc1,0xc2,0xc3,0xc4,0xc5,0xc6,0xc7};
        for (int i = 0; i < 32; i++) key[i] = 0x80 + i;
        const char *pt = "Ladies and Gentlemen of the class of '99: If I could offer you only one tip for the future, sunscreen would be it.";
        int ptlen = (int)strlen(pt);
        static const uint8_t want_ct[114] = {
            0xd3,0x1a,0x8d,0x34,0x64,0x8e,0x60,0xdb,0x7b,0x86,0xaf,0xbc,0x53,0xef,0x7e,0xc2,
            0xa4,0xad,0xed,0x51,0x29,0x6e,0x08,0xfe,0xa9,0xe2,0xb5,0xa7,0x36,0xee,0x62,0xd6,
            0x3d,0xbe,0xa4,0x5e,0x8c,0xa9,0x67,0x12,0x82,0xfa,0xfb,0x69,0xda,0x92,0x72,0x8b,
            0x1a,0x71,0xde,0x0a,0x9e,0x06,0x0b,0x29,0x05,0xd6,0xa5,0xb6,0x7e,0xcd,0x3b,0x36,
            0x92,0xdd,0xbd,0x7f,0x2d,0x77,0x8b,0x8c,0x98,0x03,0xae,0xe3,0x28,0x09,0x1b,0x58,
            0xfa,0xb3,0x24,0xe4,0xfa,0xd6,0x75,0x94,0x55,0x85,0x80,0x8b,0x48,0x31,0xd7,0xbc,
            0x3f,0xf4,0xde,0xf0,0x8e,0x4b,0x7a,0x9d,0xe5,0x76,0xd2,0x65,0x86,0xce,0xc6,0x4b,
            0x61,0x16};
        static const uint8_t want_tag[16] = {
            0x1a,0xe1,0x0b,0x59,0x4f,0x09,0xe2,0x6a,0x7e,0x90,0x2e,0xcb,0xd0,0x60,0x06,0x91};
        uint8_t ct[128], tag[16], rt[128];
        aead_seal(key, nonce, aad, 12, (const uint8_t *)pt, ptlen, ct, tag);
        int p = eq(ct, want_ct, ptlen) && eq(tag, want_tag, 16);
        /* round-trip open */
        p &= (aead_open(key, nonce, aad, 12, ct, ptlen, tag, rt) == 0) && eq(rt, (const uint8_t*)pt, ptlen);
        ok &= p;
        printf("[crypto] aead     %s\n", p ? "ok" : "FAIL");
    }

    /* X25519 (RFC 7748 section 5.2) */
    {
        static const uint8_t scalar[32] = {
            0xa5,0x46,0xe3,0x6b,0xf0,0x52,0x7c,0x9d,0x3b,0x16,0x15,0x4b,0x82,0x46,0x5e,0xdd,
            0x62,0x14,0x4c,0x0a,0xc1,0xfc,0x5a,0x18,0x50,0x6a,0x22,0x44,0xba,0x44,0x9a,0xc4};
        static const uint8_t point[32] = {
            0xe6,0xdb,0x68,0x67,0x58,0x30,0x30,0xdb,0x35,0x94,0xc1,0xa4,0x24,0xb1,0x5f,0x7c,
            0x72,0x66,0x24,0xec,0x26,0xb3,0x35,0x3b,0x10,0xa9,0x03,0xa6,0xd0,0xab,0x1c,0x4c};
        static const uint8_t want[32] = {
            0xc3,0xda,0x55,0x37,0x9d,0xe9,0xc6,0x90,0x8e,0x94,0xea,0x4d,0xf2,0x8d,0x08,0x4f,
            0x32,0xec,0xcf,0x03,0x49,0x1c,0x71,0xf7,0x54,0xb4,0x07,0x55,0x77,0xa2,0x85,0x52};
        uint8_t out[32];
        x25519(out, scalar, point);
        int p = eq(out, want, 32); ok &= p;
        printf("[crypto] x25519   %s\n", p ? "ok" : "FAIL");
    }

    printf("[crypto] self-test %s\n", ok ? "PASSED" : "FAILED");
    return ok ? 0 : -1;
}
